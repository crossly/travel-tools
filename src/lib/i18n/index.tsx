export function interpolate(template: string, values?: Record<string, string | number>) {
  if (!values) return template
  return template.replace(/\{(\w+)\}/g, (_, key) => String(values[key] ?? ''))
}

export function detectInitialLocale(pathLocale?: string): Locale {
  if (isLocale(pathLocale)) return pathLocale
  const stored = readStoredLocale()
  if (stored) return stored
  if (typeof navigator !== 'undefined') {
    const preferred = navigator.language
    if (preferred.toLowerCase().startsWith('en')) return 'en-US'
  }
  return DEFAULT_LOCALE
}

type I18nContextValue = {
  locale: Locale
  t: (key: string, values?: Record<string, string | number>) => string
  tError: (code: string) => string
  setLocale: (locale: Locale) => void
}

const I18nContext = createContext<I18nContextValue | null>(null)

export function I18nProvider({
  children,
  locale,
  onLocaleChange,
}: {
  children: ReactNode
  locale: Locale
  onLocaleChange: (locale: Locale) => void
}) {
  const value = useMemo<I18nContextValue>(
    () => ({
      locale,
      t: (key, values) => interpolate(messages[locale][key] ?? messages[DEFAULT_LOCALE][key] ?? key, values),
      tError: (code) => messages[locale][`error.${code}`] ?? messages[DEFAULT_LOCALE][`error.${code}`] ?? code,
      setLocale: (nextLocale) => {
        writeStoredLocale(nextLocale)
        onLocaleChange(nextLocale)
      },
    }),
    [locale, onLocaleChange],
  )

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const context = useContext(I18nContext)
  if (!context) throw new Error('useI18n must be used within I18nProvider')
  return context
}
