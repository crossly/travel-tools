export function detectLocaleFromNavigator(): Locale {
  if (typeof navigator === 'undefined') return 'zh-CN';
  return navigator.language.toLowerCase().startsWith('zh') ? 'zh-CN' : 'en-US';
}

export function readLocale(): Locale {
  if (typeof localStorage === 'undefined') return detectLocaleFromNavigator();
  const saved = localStorage.getItem(STORAGE_KEYS.locale);
  return saved === 'en-US' || saved === 'zh-CN' ? saved : detectLocaleFromNavigator();
}

export function writeLocale(locale: Locale): void {
  if (typeof localStorage === 'undefined') return;
  localStorage.setItem(STORAGE_KEYS.locale, locale);
}

export function translate(locale: Locale, key: TranslationKey, vars?: Record<string, string | number>): string {
  let text = messages[locale][key] ?? messages['en-US'][key] ?? key;
  if (!vars) return text;
  for (const [name, value] of Object.entries(vars)) {
    text = text.replace(`{${name}}`, String(value));
  }
  return text;
}

